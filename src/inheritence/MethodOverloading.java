package inheritence;

public class MethodOverloading {


    // any number of methods with the same name but they take different number of arguments
    public int add(int a,int b)
    {
        return a+b;
    }

    public int add(int a,int b,int c)
    {
        return a+b+c;
    }


    public int add(int a,int b,int c,int d)
    {
        return a+b+c+d;
    }





}
